package com.b4brar.puntrans;

public class Akkhar {
   private String mPunjabi;
   private String mEnglish;
   Akkhar(String punjabi, String english){
	  mPunjabi = punjabi;
	  mEnglish = english;
   }
   public String getPunjabi(){
	   return mPunjabi;
   }
	public String getEnglish(){
		return mEnglish;
	}
}
